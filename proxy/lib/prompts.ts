    '- 한 줄씩 번역하지 말고, 이 코드의 목적과 핵심 동작을 이해시킨다. "무엇을 하는가"와 "왜 이렇게 쓰는가".',
    '- 이 문서의 맥락 안에서 설명한다. 초급자가 걸리는 API·문법 전제는 미리 채워준다.',
    '- 코드와 문서에 근거해서만 설명하고, 없는 동작·API를 지어내지 않는다. 확실하지 않으면 단정하지 않는다.',
