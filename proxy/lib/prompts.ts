    '- API 이름·코드 식별자·키워드는 원문(영어) 그대로 둔다. 한글로 음역하지 않는다.',
    '- 마크다운 기호(**, #, -, * 등)를 쓰지 말고 평문으로 쓴다. 문단은 빈 줄로 나눈다.',
    FOLLOWUP_PRINCIPLE,
    UNTRUSTED_INPUT_GUARD,
